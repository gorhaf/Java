public class Main {

    public static void main(String[] args) {
        try {
            // 缓冲流
            BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
            // 记录一行数据
            String line = null;
            // 循环
            while (!"退出".equals(line)) {
                // 获取键盘输入
                line = reader.readLine();
                // 输出字符
                System.out.println(line);
            }
        } catch (IOException e) {
            throw new RuntimeException(e);
        }
    }
}