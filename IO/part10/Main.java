public class Main {

    public static void main(String[] args) {
        // 输出流
        OutputStream out = null;
        try {
            // 创建File对象
            File file = new File("/Users/admin/Downloads/Java/人人都是程序员.txt");
            // 当文件不存在时
            if (!file.exists()) {
                // 创建目录
                if (!file.getParentFile().mkdirs()) {
                    System.out.println("目录创建失败");
                    return;
                }
            }
            // 初始化输出流
            out = new FileOutputStream(file);
            // 写入数据
            out.write("看动画，学编程，不枯燥".getBytes());
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            // 当输出流不为空时
            if (out != null) {
                try {
                    // 关闭输出流
                    out.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
    }
}